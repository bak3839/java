package week3;

import java.util.Scanner;

public class EvenOdd {
	public static void main(String args[]) {
		Scanner input = new Scanner(System.in);
		int num;
		
		System.out.print("정수를 입력하시오:");
		num=input.nextInt();
		
		if(num % 2 == 0)
			System.out.println("짝수입니다.");
		else
			System.out.println("홀수입니다.");
	}
}
